--Create an SQL query to identify list of passengers based on thier method of payment. 
--For each method of payment, Airline can plan to offer corresponding incentives/rewards to passengers.

select p.passenger_id, p.passenger_first_name, p.passenger_last_name, p1.payment_method 
from passenger p
join trip_ticket t
on p.passenger_id=t.passenger_id
join payment p1
on t.payment_id=p1.payment_id
where p1.payment_method='&enter_payment_method';


--Create a query to identify the longest flight and its price. Order by decresing price.
SELECT f.flight_id, (f.arrival_time - f.departure_time) as duration, p.amount
FROM trip_ticket tt JOIN flight f
ON(tt.flight_id = f.flight_id) 
JOIN payment p 
ON(tt.payment_id = p.payment_id)
ORDER by duration desc
;


--Create a SQL query to know most flown/preffered airline

SELECT count(tt.passenger_id) as "Passenger Count", A.airline_name
FROM trip_ticket TT JOIN flight F
ON (TT.flight_id = f.flight_id)
RIGHT JOIN airline A
ON(F.airline_id = A.airline_id)
RIGHT JOIN passenger p
ON ( p.passenger_id = TT.passenger_id) 
GROUP BY A.airline_name
ORDER BY count(tt.passenger_id) desc
;


--Create a SQL query to identify trip search results those which yielded a succesfull ticket booking. Output the search id and the ticket id for same passengers with origin and destination city.

SELECT result2.search_id, result2.origin_city, result2.destination_city, result1.ticket_id
FROM (
        SELECT tt.ticket_id as ticket_id, f.origin_city as origin_city, f.destination_city as destination_city, f.departure_date as departure_date, f.airline_id as airline_id, tt.passenger_id as passenger_id 
        FROM trip_ticket tt JOIN flight f 
        ON(tt.flight_id = f.flight_id)
    ) result1
    JOIN 
    (
        SELECT ts.search_id as search_id, ts.origin_city as origin_city, ts.destination_city as destination_city, ts.departure_date as departure_date, p.passenger_id as passenger_id 
        FROM trip_search ts JOIN passenger p
        ON(ts.passenger_id = p.passenger_id)
    )result2
ON(result1.passenger_id = result2.passenger_id)    
WHERE (result1.origin_city = result2.origin_city and result1.destination_city = result2.destination_city and result1.departure_date = result2.departure_date)
;

